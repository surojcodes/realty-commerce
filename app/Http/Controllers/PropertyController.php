                return back()->with('error','City "'.$keyword.'" NOT FOUND!');
            }
            $totalMatchedListing=$results->getTotalResultsCount()-1;
        }

        $results = $this->fieldRename($results);

        $data=[];

        foreach($results as $result){
            $id = $result['Matrix_Unique_ID'];
            $photo = $this->rets->getObject('Property','Photo',$results[0]['Matrix_Unique_ID'],'1',1)[0];
